public class TernaryOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        // If a is greater than b, assign a, otherwise assign b
        // variable = condition, ternary symbol (?) conditional variable ternary symbol (:) conditional variable
        int max = (a > b) ? a : b;
        System.out.println(" Maximum value is: " + max);
    }
}
