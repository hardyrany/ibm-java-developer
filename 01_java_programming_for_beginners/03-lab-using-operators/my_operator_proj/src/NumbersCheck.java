public class NumbersCheck {
    public static void main(String[] args) {

        int[] intArray = { 12, 17, 3, 165, 87, 15, 54, 123, 98, 23 };
        int additionArr = intArray[0] + intArray[1];
        int subtractionArr = intArray[2] - intArray[3];
        int multiplicationArr = intArray[4] * intArray[5];
        int divisionArr = intArray[6] / intArray[7];
        int modulusArr = intArray[8] % intArray[9];

        System.out.println(additionArr);
        System.out.println(subtractionArr);
        System.out.println(multiplicationArr);
        System.out.println(divisionArr);
        System.out.println(modulusArr);

    }
}
